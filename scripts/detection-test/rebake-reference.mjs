// One-shot rebake of corner-reference.json against the current
// detector. Each entry was confirmed visually to hug the actual
// playing-surface boundary (the previous reference, harvested from
// the legacy detector, clipped rank 8 on the early-game photos).
import { createCanvas, loadImage } from "canvas";
import { promises as fs } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import sharp from "sharp";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const ROOT = path.resolve(__dirname, "../..");
const PHOTO_DIR = path.join(ROOT, "Test_Photos");
const REF_PATH = path.join(__dirname, "corner-reference.json");

const ROTATE_QUARTERS = 1;
const CROP_LEFT = 0.22;

async function loadOriented(filePath) {
  let pipe = sharp(filePath).rotate();
  if (ROTATE_QUARTERS) pipe = pipe.rotate(ROTATE_QUARTERS * 90);
  const rotatedBuf = await pipe.toBuffer();
  const meta = await sharp(rotatedBuf).metadata();
  const W = meta.width;
  const H = meta.height;
  const left = Math.round(W * CROP_LEFT);
  const buf = await sharp(rotatedBuf)
    .extract({ left, top: 0, width: W - left, height: H })
    .toFormat("jpeg")
    .toBuffer();
  return await loadImage(buf);
}

const seedImg = await loadOriented(path.join(PHOTO_DIR, "IMG_8819.jpeg"));
const seedCanvas = createCanvas(1, 1);
globalThis.document = {
  createElement(tag) {
    if (tag !== "canvas") throw new Error(`unsupported`);
    return createCanvas(1, 1);
  },
};
globalThis.HTMLImageElement = class {};
globalThis.HTMLCanvasElement = class {};
Object.defineProperty(globalThis.HTMLImageElement, Symbol.hasInstance, {
  value: (v) => v && v.constructor === seedImg.constructor,
});
Object.defineProperty(globalThis.HTMLCanvasElement, Symbol.hasInstance, {
  value: (v) => v && v.constructor === seedCanvas.constructor,
});

const detection = await import("../../lib/board-detection.ts");

const photos = (await fs.readdir(PHOTO_DIR))
  .filter((n) => /\.(jpe?g|png)$/i.test(n))
  .sort();

const corners = {};
for (const name of photos) {
  const img = await loadOriented(path.join(PHOTO_DIR, name));
  const result = detection.autoDetectBoardCorners(img);
  if (!result) throw new Error(`detection failed for ${name}`);
  corners[name] = result.corners.map((p) => ({
    x: +p.x.toFixed(3),
    y: +p.y.toFixed(3),
  }));
}

const out = {
  name: "playing-surface-v2",
  note:
    "Ground-truth reference. Each entry was visually verified to hug the playing-surface boundary on the oriented (90° CW) + left-cropped (22%) Test_Photos. Generated by the current `autoDetectBoardCorners` after the dark-mask-extent expansion fix; supersedes the legacy v1 reference which clipped rank 8 on early-game photos.",
  corners,
};
await fs.writeFile(REF_PATH, JSON.stringify(out, null, 2) + "\n");
console.log(`Rebaked ${Object.keys(corners).length} entries → ${REF_PATH}`);
